    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        this.repaint();

        String dpi = this.textField1.getText();
        for (int i = 0; i < datos_asegurado.getContador_Aprobados(); i++) {
            if ("".equals(dpi)) {
                JOptionPane.showMessageDialog(null, "no ha ingresado un DPI para verificar si es asegurado o no asegurado");
                break;
            }
        }

        for (int i = 0; i < datos_asegurado.getContador_Aprobados(); i++) {
            if (dpi.equals(datos_asegurado.getDPI()[i])) {
                datos_asegurado.setPosicion_Referencia(i);
                MENU_ASEGURADO asegurados = new MENU_ASEGURADO();
                asegurados.setVisible(true);
                this.textField1.setText("");
                this.setVisible(false);
                //     break;
            }
        }
        System.out.println("valor de REFERENCIA: " + datos_asegurado.getPosicion_Referencia());
    }//GEN-LAST:event_jButton4ActionPerformed

